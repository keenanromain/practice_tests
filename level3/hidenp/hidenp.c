#include <unistd.h>

void	hider(char *s1, char *s2)
{
	int x;
	int y;

	x = -1;
	y = 0;
	while (s2[++x])
	{
		if (s2[x] == s1[y])
		{
			++y;
			if (s1[y] == '\0')
			{
				write(1, "1", 1);
				return ;
			}
		}
	}
	write(1, "0", 1);
}

int		main(int ac, char **av)
{
	if (ac == 3)
		hider(av[1], av[2]);
	write(1, "\n", 1);
	return (0);
}
