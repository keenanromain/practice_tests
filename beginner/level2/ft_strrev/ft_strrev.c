char		*ft_strrev(char *str)
{
    int		i;
    int		j;
	int		k;
    char	c;
    
    j = 0;
    while (str[j] != '\0')
        j++;
	k = j;
    i = -1;
    while (++i < --j)
    {
        c = str[i];
        str[i] = str[j];
        str[j] = c;
    }
	str[k] = '\0';
    return (str);
}
