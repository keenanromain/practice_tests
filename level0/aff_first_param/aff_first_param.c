#include <unistd.h>

void	ft_putstr(char *s)
{
	while (*s)
		write(1, s++, 1);
}

int		main(int ac, char **av)
{
	if (ac > 1)
		ft_putstr(av[1]);
	write(1, "\n", 1);
	return (0);
}
